package basic;

public class FloatExample {

	public static void main(String[] args) {
		
		
		float f1 = 7.12345F;
		double d1 = 7.12345;
		
		//실수 기본데이터 타입은 double
		
		System.out.println(d1);
		
		//////////////////////
		
		float f2 = 1.123456791234F;
		double d2 = 1.1234567891234;
		
		System.out.println(f2);
		System.out.println(d2);

	}

}
